import express from 'express';
import auth from '../../middlewares/auth';
import validate from '../../middlewares/validate';
import { conversationValidation } from '../../validations';
import { conversationController } from '../../controllers';

const router = express.Router();

router
  .route('/')
  .post(auth('manageConversations'), validate(conversationValidation.createConversation), conversationController.createConversation)
  .get(auth('getConversations'), validate(conversationValidation.getConversations), conversationController.getConversations);

router
  .route('/:conversationId')
  .get(auth('getConversations'), validate(conversationValidation.getConversation), conversationController.getConversation)
  .patch(auth('manageConversations'), validate(conversationValidation.updateConversation), conversationController.updateConversation)
  .delete(auth('manageConversations'), validate(conversationValidation.deleteConversation), conversationController.deleteConversation);

router
  .route('/:conversationId/messages')
  .get(auth('getConversations'), validate(conversationValidation.getConversation), conversationController.getConversationMessages);

export default router;

